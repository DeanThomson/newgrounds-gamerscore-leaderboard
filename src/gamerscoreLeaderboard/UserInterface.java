    private void updateTable() {
        ArrayList<User> users = leaderboard.getLeaderboard();
        DefaultTableModel table = (DefaultTableModel) tblLeaderboard.getModel();
        
        for (int i = table.getRowCount() - 1; i >= 0; i--) {
            table.removeRow(i);
        }
        
        for(int i=0; i<users.size(); i++) {
            User user = users.get(i);
            if (user.getPreviousRank() == 0 && user.getCurrentRank() == 0)
                table.insertRow(i, new Object[]{user.getName(), user.getGamertag()});
            else if(user.getPreviousRank() == 0 && user.getCurrentRank() != 0)
                table.insertRow(i, new Object[]{user.getName(), user.getGamertag(), null, null, user.getCurrentRank(), user.getCurrentScore(), user.getScoreDiff()});
            else if(user.getCurrentRank() == 0)
                table.insertRow(i, new Object[]{user.getName(), user.getGamertag(), user.getPreviousRank(), user.getPreviousScore()});
            else
                table.insertRow(i, new Object[]{user.getName(), user.getGamertag(), user.getPreviousRank(), user.getPreviousScore(), user.getCurrentRank(), user.getCurrentScore(), user.getScoreDiff()});
        }
    }
