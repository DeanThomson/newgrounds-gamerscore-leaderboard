    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        JTextField username = new JTextField();
        JTextField gamertag = new JTextField();
        final JComponent[] inputs = new JComponent[] {
                new JLabel("Username:"),username,
                new JLabel("Gamertag:"),gamertag,
        };
        int reply = JOptionPane.showConfirmDialog(rootPane, inputs, "Add new user", JOptionPane.PLAIN_MESSAGE);
        
        if (reply == JOptionPane.YES_OPTION) {
            leaderboard.addUser(new User(username.getText(), gamertag.getText()));
            updateTable();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selection = tblLeaderboard.getSelectedRow();
        if(selection >= 0) {
            String name = tblLeaderboard.getValueAt(selection, 0).toString();
            String tag = tblLeaderboard.getValueAt(selection, 1).toString();

            JTextField username = new JTextField();
            JTextField gamertag = new JTextField();
            username.setText(name);
            gamertag.setText(tag);

            final JComponent[] inputs = new JComponent[] {
                    new JLabel("Username:"),username,
                    new JLabel("Gamertag:"),gamertag,
            };
            int reply = JOptionPane.showConfirmDialog(rootPane, inputs, "Edit user", JOptionPane.PLAIN_MESSAGE);

            if (reply == JOptionPane.YES_OPTION) {
                leaderboard.editUser(name, username.getText(), gamertag.getText());
                updateTable();
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int[] selection = tblLeaderboard.getSelectedRows();
        for(int i : selection) {
            leaderboard.deleteUser(tblLeaderboard.getValueAt(i, 0).toString());
        }
        updateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

